    public static UniversityCode findById(Long id) {
        for(UniversityCode code : values()) {
            if(code.getId().equals(id)) {
                return code;
            }
        }
        throw new IllegalArgumentException("Unknown University Id : " + id);
    }

    public static UniversityCode findByName(String name) {
        for(UniversityCode code : values()) {
            if(code.getName().equalsIgnoreCase(name)) {
                return code;
            }
        }
        throw new IllegalArgumentException("Unknown University Name : " + name);
    }
